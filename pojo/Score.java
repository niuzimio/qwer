package com.web.teacher.pojo;

public class Score {
	private int snum;
	private String sname;
	private int cid;
	private int sco;
	
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getCid() {
		return cid;
	}
	public int getSnum() {
		return snum;
	}
	public void setSnum(int snum) {
		this.snum = snum;
	}
	public int getSco() {
		return sco;
	}
	public void setSco(int sco) {
		this.sco = sco;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
}
